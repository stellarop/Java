package stream;

// LambdaExpressionExample 클래스를 사용하기 위한 Student 클래스
public class Student {
    private String name;
    private int score;

    public Student(String name, int score){
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }
    public int getScore(){
        return score;
    }
}
