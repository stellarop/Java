package lambdaExpression;

// 람다식2를 참조하는 클래스
public class LambdaExample2St {
    private String name;
    private int es;
    private int ms;

    public LambdaExample2St(String name, int es, int ms){
        this.name = name;
        this.es = es;
        this.ms = ms;
    }

    public String getName() {
        return name;
    }

    public int getEs() {
        return es;
    }

    public int getMs() {
        return ms;
    }
}
