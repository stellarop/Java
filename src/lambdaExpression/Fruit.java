package lambdaExpression;

// OperatorMinByMaxByExample class 를 이용하기 위한 class
public class Fruit {
    private String name;
    private int price;

    public Fruit(String name, int price){
        this.name = name;
        this.price = price;
    }

    public String getName(){
        return name;
    }

    public int getPrice(){
        return price;
    }
}
